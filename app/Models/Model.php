<?php

declare(strict_types=1);

namespace App\Models;

use App\App;
use App\DB;

/**
 * Базовый класс для всех моделей.
 *
 * Предоставляет общую функциональность для работы с базой данных.
 * Все модели наследуются от этого класса и получают доступ к подключению к БД.
 */
abstract class Model
{
    /**
     * Подключение к базе данных.
     *
     * @var DB
     */
    protected DB $db;

    /**
     * Конструктор базовой модели.
     *
     * Инициализирует подключение к базе данных через статический метод App::db().
     */
    public function __construct()
    {
        $this->db = App::db();
    }
}
